package LocadoraVeiculos;

public abstract class Veiculo {

    //atributos
    private String placa;
    private int ano;

    //metodos especiais

    //get e set


    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
}
